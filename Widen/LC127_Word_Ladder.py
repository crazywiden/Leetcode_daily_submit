class Solution:
    def ladderLength(self, beginWord: str, endWord: str, wordList: List[str]) -> int:
        tot_word = wordList + [beginWord]
        N = len(tot_word)
        
        queue = [[N-1, 1]]
        visited = set([N-1])
        while queue:
            node, step = queue.pop(0)
            if tot_word[node] == endWord:
                return step
            for i in range(N):
                if i in visited:
                    continue
                if self.is_changable(tot_word[i], tot_word[node]):
                    queue.append([i, step+1])
                    visited.add(i)
        return 0
        
    def is_changable(self, word1, word2):
        change_cnt = 0
        for i, j in zip(word1, word2):
            if i != j:
                change_cnt += 1
            if change_cnt > 1:
                return False
        return True
    
        


